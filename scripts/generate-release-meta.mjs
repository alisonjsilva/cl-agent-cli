import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(scriptDir, "..");
const packageJsonPath = path.join(rootDir, "package.json");
const outputPath = path.join(rootDir, "docs", "release-meta.js");

const pkg = JSON.parse(await readFile(packageJsonPath, "utf8"));

const repositoryUrl =
  typeof pkg.repository === "string"
    ? pkg.repository
    : typeof pkg.repository?.url === "string"
      ? pkg.repository.url
      : null;

const metadata = {
  packageName: typeof pkg.name === "string" ? pkg.name : "cl-agent-cli",
  version: typeof pkg.version === "string" ? pkg.version : "0.0.0",
  repositoryUrl,
};

const contents = [
  "// Generated by scripts/generate-release-meta.mjs. Do not edit by hand.",
  `export const RELEASE_METADATA = Object.freeze(${JSON.stringify(metadata, null, 2)});`,
  "",
].join("\n");

await writeFile(outputPath, contents, "utf8");

console.log(`Updated ${path.relative(rootDir, outputPath)} for v${metadata.version}`);